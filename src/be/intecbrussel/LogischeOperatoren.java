package be.intecbrussel;

public class LogischeOperatoren {
    public static void main(String[] args){

        boolean bool1 = true, bool2 = false;

        System.out.println("value1\t\t\t\t:" + bool1);
        System.out.println("value2\t\t\t\t:" + bool2);
        System.out.println("value1 && value2\t:" + (bool1&&bool2));
        System.out.println("value1 || value2 \t:" + (bool1||bool2));
        System.out.println("!value1\t\t\t\t:" + !bool1);
        System.out.println("!value2\t\t\t\t:" + !bool2);

    }
}
